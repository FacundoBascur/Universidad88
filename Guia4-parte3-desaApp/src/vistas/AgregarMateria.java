    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            Conexion con = new Conexion("jdbc:mariadb://localhost:3306/universidaddelapunta", "root", "");
            con.buscarConexion();
            MateriaData mat = new MateriaData(con);

            if (jtNombre.getText().isEmpty() || jtAnio.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se admiten campos vacios");

            } else if (Integer.parseInt(jtAnio.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Solo numeros enteros positivos");

            } else {

                String[] lista = {"Activo", "Inactivo"};
                int opcion = JOptionPane.showOptionDialog(null, "  Elige una opcion", "", 0, JOptionPane.QUESTION_MESSAGE, null, lista, "");

                boolean act = opcion == 0;

                mat.guardarMateria(new Materia(jtNombre.getText(), Integer.parseInt(jtAnio.getText()), act));

            }

            jtNombre.setText("");
            jtAnio.setText("");

        } catch (NumberFormatException | NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Error, verifique sus respuestas");

        }
    }//GEN-LAST:event_jButton1ActionPerformed


